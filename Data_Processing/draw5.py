def map_amino_acid_sequence(sequence):
    """
    Map the amino acid sequence to a 441-dimensional vector
    """
    mapped_sequence = []
    for amino_acid in sequence:
        mapped_sequence.extend(amino_acid_mapping[amino_acid])
    return mapped_sequence

def process_txt_file(input_file, output_file):
    """
    Read a txt file, map a 21-dimensional amino acid sequence to a 441-dimensional vector, and save it as a CSV file
    """
    with open(input_file, 'r') as infile, open(output_file, 'w', newline='') as outfile:
        reader = csv.reader(infile)
        writer = csv.writer(outfile)

        for row_num, row in enumerate(reader, 1):
            # Process only even-numbered rows
            if row_num % 2 == 0:
                amino_acid_sequence = row[0]
                mapped_sequence = map_amino_acid_sequence(amino_acid_sequence)
                writer.writerow(mapped_sequence)

